package sorting;

public interface SimpleSorting {
	/**
	 * In-place bubble sort. 
	 */
	public void bubbleSort(int[] array);
	
	/**
	 * In-place selection sort. 
	 */
	public void selectionSort(int[] array);
	
	/**
	 * In-place insertion sort. 
	 */
	public void insertionSort(int[] array);
	
}